void main() {
  // Operator Aritmatika

  var first = 30;
  var second = 3;

  print(first + second); // tambah
  print(first - second); // kurang
  print(first * second); // kali
  print(first / second); // bagi double
  print(first ~/ second); // bagi integer
  print(first % second); // sisa bagi
}
